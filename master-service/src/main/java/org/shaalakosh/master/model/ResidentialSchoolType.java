package org.shaalakosh.master.model;

public class ResidentialSchoolType {
	
	private int residentialSchoolTypeID;
	
	private String residentialSchoolTypeDesc;
	
	private boolean isActive;

	public int getResidentialSchoolTypeID() {
		return residentialSchoolTypeID;
	}

	public void setResidentialSchoolTypeID(int residentialSchoolTypeID) {
		this.residentialSchoolTypeID = residentialSchoolTypeID;
	}

	public String getResidentialSchoolTypeDesc() {
		return residentialSchoolTypeDesc;
	}

	public void setResidentialSchoolTypeDesc(String residentialSchoolTypeDesc) {
		this.residentialSchoolTypeDesc = residentialSchoolTypeDesc;
	}

	public boolean isActive() {
		return isActive;
	}

	public void setActive(boolean isActive) {
		this.isActive = isActive;
	}
	
	

}
