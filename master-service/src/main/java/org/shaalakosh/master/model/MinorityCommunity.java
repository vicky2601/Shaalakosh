package org.shaalakosh.master.model;

public class MinorityCommunity {

	private int minorityCommunityID;
	
	private String minorityCommunityDesc;
	
	private boolean isActive;

	public int getMinorityCommunityID() {
		return minorityCommunityID;
	}

	public void setMinorityCommunityID(int minorityCommunityID) {
		this.minorityCommunityID = minorityCommunityID;
	}

	public String getMinorityCommunityDesc() {
		return minorityCommunityDesc;
	}

	public void setMinorityCommunityDesc(String minorityCommunityDesc) {
		this.minorityCommunityDesc = minorityCommunityDesc;
	}

	public boolean isActive() {
		return isActive;
	}

	public void setActive(boolean isActive) {
		this.isActive = isActive;
	}
	
	
}
